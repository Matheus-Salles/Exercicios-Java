public class Angulo {
	
	public static double  converteAngulo (double num) {
		return Math.toRadians(num);
	}
	public static double funcaoSeno (double num) {
		return Math.sin(num);
	}
	public static double funcaoCoseno (double num) {
		return Math.cos(num);
	}
	public static double funcaoTangente (double num) {
		return Math.tan(num);
	}
	public static double funcaoCotangente (double num) {
		return 1/Math.tan(num);
	}
}

